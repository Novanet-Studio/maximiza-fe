export interface FormOption {
  value: string | number;
  label: string;
}

export const getLabel = (
  value: string | number | undefined,
  options: FormOption[],
): string => {
  if (!value) return "";
  const found = options.find((opt) => opt.value === value);
  return found ? found.label : String(value);
};

